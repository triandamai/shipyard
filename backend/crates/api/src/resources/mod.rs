/// POST /projects/:project_id/networks
async fn create_network(
    auth_user: AuthUser,
    Path(project_id): Path<Uuid>,
    State(state): State<AppState>,
    Json(body): Json<CreateNetworkRequest>,
) -> Result<(StatusCode, Json<ApiResponse<Network>>), ApiAppError> {
    require_project_permission(&state.db, auth_user.user_id, project_id, "network:write").await.map_err(ApiAppError)?;
    if body.name.is_empty() {
        return Err(ApiAppError(AppError::BadRequest("name is required".to_string())));
    }

    let org_row: Option<(Uuid,)> = sqlx::query_as::<_, (Uuid,)>(
        "SELECT org_id FROM projects WHERE id = $1",
    )
    .bind(project_id)
    .fetch_optional(&state.db)
    .await
    .map_err(|e| ApiAppError(AppError::Database(e.to_string())))?;

    let (org_id,) = org_row.ok_or_else(|| {
        ApiAppError(AppError::NotFound(format!("Project '{}' not found", project_id)))
    })?;

    let driver = body.driver.unwrap_or_else(|| "overlay".to_string());
    let subnet = body.subnet.unwrap_or_default();

    // Create the Docker network first; this returns the Docker-assigned network ID.
    let docker_network_id = state.docker.create_network(&body.name, &driver).await
        .map_err(|e| ApiAppError(AppError::Docker(e.to_string())))?;

    let network = sqlx::query_as::<_, Network>(
        "INSERT INTO networks (id, project_id, name, driver, subnet, docker_network_id, created_at)
         VALUES ($1, $2, $3, $4, $5, $6, NOW())
         RETURNING id, project_id, name, driver, subnet, docker_network_id, created_at",
    )
    .bind(Uuid::now_v7())
    .bind(project_id)
    .bind(&body.name)
    .bind(&driver)
    .bind(&subnet)
    .bind(&docker_network_id)
    .fetch_one(&state.db)
    .await
    .map_err(|e| {
        let msg = e.to_string();
        // Best-effort cleanup of the Docker network on DB failure.
        let docker_id = docker_network_id.clone();
        let docker = state.docker.clone();
        tokio::spawn(async move {
            if let Err(re) = docker.remove_network(&docker_id).await {
                tracing::warn!("Failed to remove orphaned Docker network '{docker_id}': {re}");
            }
        });
        if msg.contains("unique") || msg.contains("duplicate") {
            ApiAppError(AppError::Conflict(format!(
                "A network named '{}' already exists in this project",
                body.name
            )))
        } else {
            ApiAppError(AppError::Database(msg))
        }
    })?;

    let topic = shipyard_mqtt::topics::topology(org_id, project_id);
    let payload = MqttPayload::new("topology.changed")
        .with_meta(serde_json::json!({ "resource": "network", "network_id": network.id }));
    state.mqtt.publish_status(&topic, &payload).await.ok();

    Ok((StatusCode::CREATED, Json(ApiResponse::ok(network))))
}

