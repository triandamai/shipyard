// ─── Traefik dynamic config writer ───────────────────────────────────────────

/// nip.io and traefik.me are wildcard DNS convenience domains.
/// They must never use certResolver — each unique subdomain would consume a LE
/// certificate slot, exhausting the rate limit very quickly.
/// These domains are served over plain HTTP instead.
fn is_convenience_domain(hostname: &str) -> bool {
    hostname.ends_with(".nip.io") || hostname.ends_with(".traefik.me")
}

/// Converts a hostname into a safe Traefik router/service name.
/// Each domain gets a unique name, avoiding duplicate YAML keys when a service
/// has multiple domains attached.
fn hostname_to_router_name(hostname: &str) -> String {
    hostname
        .chars()
        .map(|c| if c.is_ascii_alphanumeric() { c } else { '-' })
        .collect::<String>()
        .to_ascii_lowercase()
}

/// Regenerates the Traefik file-provider dynamic config for a service's domains.
///
/// Writes `{dir}/{slug}.yml`. If there are no domains the file is removed.
/// Silently skips if `dynamic_config_dir` is not configured.
async fn sync_traefik_dynamic_config(
    db: &sqlx::PgPool,
    service_id: Uuid,
    label_prefix: &str,
    entrypoint_http: &str,
    entrypoint_https: &str,
    dynamic_config_dir: Option<&str>,
) {
    let Some(dir) = dynamic_config_dir else { return };

    // Resolve slug and type for file name / upstream selection
    let (slug, svc_type): (String, String) = sqlx::query_as(
        "SELECT slug, type::text FROM services WHERE id = $1",
    )
    .bind(service_id)
    .fetch_optional(db)
    .await
    .ok()
    .flatten()
    .unwrap_or_else(|| (service_id.to_string(), String::new()));

    let path = std::path::Path::new(dir).join(format!("{slug}.yml"));

    // Fetch all current domains for this service
    let rows: Vec<(String, bool, String, Option<i32>, String)> = match sqlx::query_as(
        "SELECT hostname, tls_enabled, cert_provider, port, traefik_router_name
         FROM domains WHERE service_id = $1 ORDER BY created_at ASC",
    )
    .bind(service_id)
    .fetch_all(db)
    .await
    {
        Ok(r) => r,
        Err(e) => {
            tracing::warn!("sync_traefik_dynamic_config: DB error: {e}");
            return;
        }
    };

    if rows.is_empty() {
        let _ = tokio::fs::remove_file(&path).await;
        tracing::info!("Removed Traefik dynamic config (no domains): {path:?}");
        return;
    }

    // Static sites are all served by the shared nginx container, not by a per-service
    // Docker Swarm service. Everything else routes to its own Swarm service.
    let upstream_host = if svc_type == "static" {
        "shipyard-nginx-static".to_string()
    } else {
        format!("{label_prefix}-{service_id}")
    };

    let mut out = String::from("# Auto-generated by Shipyard — do not edit manually\n");
    out.push_str("http:\n");
    out.push_str("  routers:\n");

    for (hostname, tls_enabled, cert_provider, _port, _stored_router_name) in &rows {
        // Derive a unique router name from the hostname so multiple domains on the
        // same service never produce duplicate YAML keys (which silently drop entries).
        let rn = hostname_to_router_name(hostname);
        let convenience = is_convenience_domain(hostname);

        // HTTP router (global entryPoint redirect sends plain-HTTP requests to HTTPS)
        let _ = write!(out, "    {rn}-http:\n");
        let _ = write!(out, "      rule: \"Host(`{hostname}`)\"\n");
        let _ = write!(out, "      entryPoints:\n");
        let _ = write!(out, "        - {entrypoint_http}\n");
        let _ = write!(out, "      service: {rn}\n");

        // Always create an HTTPS router so the domain is reachable when the global
        // HTTP→HTTPS redirect is active. Convenience and tls_enabled=false domains
        // use a self-signed fallback cert; real custom domains use certResolver.
        let _ = write!(out, "    {rn}-https:\n");
        let _ = write!(out, "      rule: \"Host(`{hostname}`)\"\n");
        let _ = write!(out, "      entryPoints:\n");
        let _ = write!(out, "        - {entrypoint_https}\n");
        // shipyard-error-pages replaces 404 responses with the branded Shipyard HTML page.
        let _ = write!(out, "      middlewares:\n");
        let _ = write!(out, "        - shipyard-error-pages@file\n");
        if convenience || !*tls_enabled {
            // nip.io / traefik.me / tls_enabled=false: self-signed cert (no LE slot consumed).
            let _ = write!(out, "      tls: {{}}\n");
        } else {
            // Real custom domain with TLS: Let's Encrypt via certResolver.
            let _ = write!(out, "      tls:\n");
            let _ = write!(out, "        certResolver: {cert_provider}\n");
        }
        let _ = write!(out, "      service: {rn}\n");
    }

    out.push_str("  services:\n");

    for (hostname, _, _, port, _) in &rows {
        let rn = hostname_to_router_name(hostname);
        let backend_port = port.unwrap_or(80);
        let _ = write!(out, "    {rn}:\n");
        let _ = write!(out, "      loadBalancer:\n");
        let _ = write!(out, "        servers:\n");
        let _ = write!(out, "          - url: \"http://{upstream_host}:{backend_port}\"\n");
    }

    if let Err(e) = tokio::fs::create_dir_all(dir).await {
        tracing::warn!("sync_traefik_dynamic_config: could not create dir '{dir}': {e}");
        return;
    }
    if let Err(e) = tokio::fs::write(&path, out.as_bytes()).await {
        tracing::warn!("sync_traefik_dynamic_config: could not write '{path:?}': {e}");
    } else {
        tracing::info!("Wrote Traefik dynamic config: {path:?}");
    }
}

/// Regenerates the nginx `server_name` line in the static site's conf file.
///
/// Called whenever domains are added or removed so nginx knows which hostnames to
/// accept for this site without waiting for the next deployment.
async fn sync_static_nginx_conf(db: &sqlx::PgPool, service_id: Uuid, data_dir: &str) {
    // Only applies to static services
    let svc_type: Option<String> = sqlx::query_scalar("SELECT type::text FROM services WHERE id = $1")
        .bind(service_id)
        .fetch_optional(db)
        .await
        .ok()
        .flatten();
    if svc_type.as_deref() != Some("static") {
        return;
    }

    let slug: Option<String> = sqlx::query_scalar("SELECT slug FROM services WHERE id = $1")
        .bind(service_id)
        .fetch_optional(db)
        .await
        .ok()
        .flatten();
    let Some(slug) = slug else { return };

    let sites_base = format!("{data_dir}/static");
    let conf_path = format!("{sites_base}/conf.d/{slug}.conf");

    let existing = match tokio::fs::read_to_string(&conf_path).await {
        Ok(c) => Some(c),
        Err(_) => None,
    };

    // Fetch the current full domain list (after the DB mutation that triggered this call)
    let domains: Vec<String> = sqlx::query_scalar(
        "SELECT hostname FROM domains WHERE service_id = $1 ORDER BY created_at ASC",
    )
    .bind(service_id)
    .fetch_all(db)
    .await
    .unwrap_or_default();

    if domains.is_empty() {
        // All domains removed — delete the conf so the app is no longer served on a wildcard.
        // The default_server block will return 404 for requests without a known hostname.
        if let Err(e) = tokio::fs::remove_file(&conf_path).await {
            if e.kind() != std::io::ErrorKind::NotFound {
                tracing::warn!("sync_static_nginx_conf: could not remove '{conf_path}': {e}");
            }
        }
    } else {
        let updated = match existing {
            Some(ref existing_conf) => {
                // Splice new server_name into existing conf, preserving all other directives
                let server_name_value = domains.join(" ");
                existing_conf
                    .lines()
                    .map(|line| {
                        if line.trim_start().starts_with("server_name ") {
                            format!("    server_name {server_name_value};")
                        } else {
                            line.to_string()
                        }
                    })
                    .collect::<Vec<_>>()
                    .join("\n")
            }
            None => {
                // Conf missing — generate from scratch using the canonical serve root.
                // nginx handles a not-yet-existing root gracefully (returns 404) so we
                // don't gate on whether the deployment directory exists yet.
                let serve_root = format!("{sites_base}/{service_id}/current/public");
                shipyard_engine::static_site::render_nginx_site_conf(
                    &service_id.to_string(),
                    &domains,
                    &serve_root,
                    &sites_base,
                    &Default::default(),
                )
            }
        };

        // Ensure conf.d exists (may not exist if nginx container hasn't been started yet).
        let conf_dir = std::path::Path::new(&conf_path)
            .parent()
            .map(|p| p.to_string_lossy().into_owned())
            .unwrap_or_default();
        if !conf_dir.is_empty() {
            if let Err(e) = tokio::fs::create_dir_all(&conf_dir).await {
                tracing::warn!("sync_static_nginx_conf: could not create conf dir '{conf_dir}': {e}");
                return;
            }
        }

        if let Err(e) = tokio::fs::write(&conf_path, updated.as_bytes()).await {
            tracing::warn!("sync_static_nginx_conf: could not write '{conf_path}': {e}");
            return;
        }
        tracing::info!("sync_static_nginx_conf: wrote {conf_path}");
    }

    let reload = tokio::process::Command::new("docker")
        .args(["exec", "shipyard-nginx-static", "nginx", "-s", "reload"])
        .output()
        .await;
    match reload {
        Ok(o) if o.status.success() => {
            tracing::info!("sync_static_nginx_conf: nginx reloaded for service {service_id}");
        }
        Ok(o) => {
            let stderr = String::from_utf8_lossy(&o.stderr);
            tracing::warn!("sync_static_nginx_conf: nginx reload non-zero: {stderr}");
        }
        Err(e) => {
            tracing::warn!("sync_static_nginx_conf: docker not in PATH: {e}");
        }
    }
}

// ─── Request types ────────────────────────────────────────────────────────────

#[derive(Debug, Deserialize)]
pub struct CreateDomainRequest {
    pub hostname: String,
    #[serde(default = "default_true")]
    pub tls_enabled: bool,
    #[serde(default = "default_cert_provider")]
    pub cert_provider: String,
    pub port: Option<i32>,
}

fn default_cert_provider() -> String {
    "letsencrypt".to_string()
}

#[derive(Debug, Deserialize)]
pub struct CreateVolumeRequest {
    pub name: String,
    pub mount_path: String,
    pub driver: Option<String>,
    pub size_mb: Option<i32>,
}

#[derive(Debug, Deserialize)]
pub struct CreateNetworkRequest {
    pub name: String,
    pub driver: Option<String>,
    pub subnet: Option<String>,
}

#[derive(Debug, Deserialize)]
pub struct AttachServiceRequest {
    pub service_id: Uuid,
}

fn default_true() -> bool {
    true
}

// ─── Router ───────────────────────────────────────────────────────────────────

pub fn routes() -> Router<AppState> {
    Router::new()
        // Domains
        .route(
            "/services/:service_id/domains",
            get(list_domains).post(create_domain),
        )
        .route(
            "/services/:service_id/domains/:domain_id",
            delete(delete_domain),
        )
        .route(
            "/services/:service_id/domains/:domain_id/check-dns",
            get(check_domain_dns),
        )
        // Service-scoped volumes (attach to a specific service)
        .route(
            "/services/:service_id/volumes",
            get(list_volumes).post(create_volume),
        )
        .route(
            "/services/:service_id/volumes/:volume_id",
            delete(delete_volume),
        )
        // Project-scoped standalone volumes
        .route(
            "/projects/:project_id/volumes",
            get(list_project_volumes).post(create_project_volume),
        )
        .route(
            "/projects/:project_id/volumes/:volume_id",
            get(get_project_volume).delete(delete_project_volume),
        )
        .route(
            "/projects/:project_id/volumes/:volume_id/attach",
            post(attach_volume_to_service),
        )
        // Networks
        .route(
            "/projects/:project_id/networks",
            get(list_networks).post(create_network),
        )
        .route(
            "/projects/:project_id/networks/:network_id",
            get(get_network).delete(delete_network),
        )
        .route(
            "/projects/:project_id/networks/:network_id/attach",
            post(attach_service),
        )
        .route(
            "/projects/:project_id/networks/:network_id/services/:service_id",
            delete(detach_service),
        )
        .route(
            "/services/:service_id/networks",
            get(list_service_networks),
        )
}

