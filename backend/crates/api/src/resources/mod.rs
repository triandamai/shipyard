/// POST /services/:service_id/domains
async fn create_domain(
    auth_user: AuthUser,
    Path(service_id): Path<Uuid>,
    State(state): State<AppState>,
    Json(body): Json<CreateDomainRequest>,
) -> Result<(StatusCode, Json<ApiResponse<Domain>>), ApiAppError> {
    require_service_permission(&state.db, auth_user.user_id, service_id, "domain:write").await.map_err(ApiAppError)?;
    if body.hostname.is_empty() {
        return Err(ApiAppError(AppError::BadRequest("hostname is required".to_string())));
    }

    // Verify service exists
    let service_exists: Option<(bool,)> = sqlx::query_as::<_, (bool,)>(
        "SELECT TRUE FROM services WHERE id = $1",
    )
    .bind(service_id)
    .fetch_optional(&state.db)
    .await
    .map_err(|e| ApiAppError(AppError::Database(e.to_string())))?;

    if service_exists.is_none() {
        return Err(ApiAppError(AppError::NotFound(format!(
            "Service '{}' not found",
            service_id
        ))));
    }

    let domain_id = Uuid::now_v7();
    // Router name is hostname-derived (unique per domain) so multiple domains on
    // the same service never collide in the Traefik YAML file.
    let traefik_router_name = hostname_to_router_name(&body.hostname);

    // Convenience wildcard-DNS domains must never request LE certs.
    let tls_enabled = body.tls_enabled && !is_convenience_domain(&body.hostname);

    let domain = sqlx::query_as::<_, Domain>(
        "INSERT INTO domains (id, service_id, hostname, tls_enabled, traefik_router_name, cert_provider, port, created_at)
         VALUES ($1, $2, $3, $4, $5, $6, $7, NOW())
         RETURNING id, service_id, hostname, tls_enabled, traefik_router_name, cert_provider, port, created_at",
    )
    .bind(domain_id)
    .bind(service_id)
    .bind(&body.hostname)
    .bind(tls_enabled)
    .bind(&traefik_router_name)
    .bind(&body.cert_provider)
    .bind(body.port)
    .fetch_one(&state.db)
    .await
    .map_err(|e| {
        let msg = e.to_string();
        if msg.contains("unique") || msg.contains("duplicate") {
            ApiAppError(AppError::Conflict(format!(
                "Domain '{}' already exists",
                body.hostname
            )))
        } else {
            ApiAppError(AppError::Database(msg))
        }
    })?;

    // Write Traefik file-provider config (no-op when dynamic_config_dir is unset)
    sync_traefik_dynamic_config(
        &state.db,
        service_id,
        &state.config.docker.label_prefix,
        &state.config.traefik.entrypoint_http,
        &state.config.traefik.entrypoint_https,
        state.config.traefik.dynamic_config_dir.as_deref(),
    ).await;

    // For static sites: update the nginx server_name so the site responds on the new domain immediately.
    sync_static_nginx_conf(&state.db, service_id, &state.config.data_dir).await;

    // Publish MQTT topology event so the canvas adds the new domain node.
    if let Ok(Some((project_id, org_id))) = sqlx::query_as::<_, (Uuid, Uuid)>(
        "SELECT p.id, p.org_id
         FROM services s
         JOIN projects p ON p.id = s.project_id
         WHERE s.id = $1",
    )
    .bind(service_id)
    .fetch_optional(&state.db)
    .await
    {
        let topic = shipyard_mqtt::topics::topology(org_id, project_id);
        let payload = MqttPayload::new("topology.changed")
            .with_meta(serde_json::json!({ "resource": "domain", "domain_id": domain.id }));
        state.mqtt.publish_status(&topic, &payload).await.ok();
    }

    Ok((StatusCode::CREATED, Json(ApiResponse::ok(domain))))
}

/// DELETE /services/:service_id/domains/:domain_id
async fn delete_domain(
    auth_user: AuthUser,
    Path((service_id, domain_id)): Path<(Uuid, Uuid)>,
    State(state): State<AppState>,
) -> Result<Json<ApiResponse<serde_json::Value>>, ApiAppError> {
    require_service_permission(&state.db, auth_user.user_id, service_id, "domain:write").await.map_err(ApiAppError)?;
    let rows_affected = sqlx::query(
        "DELETE FROM domains WHERE id = $1 AND service_id = $2",
    )
    .bind(domain_id)
    .bind(service_id)
    .execute(&state.db)
    .await
    .map_err(|e| ApiAppError(AppError::Database(e.to_string())))?
    .rows_affected();

    if rows_affected == 0 {
        return Err(ApiAppError(AppError::NotFound(format!(
            "Domain '{}' not found",
            domain_id
        ))));
    }

    // Regenerate Traefik file-provider config after removal
    sync_traefik_dynamic_config(
        &state.db,
        service_id,
        &state.config.docker.label_prefix,
        &state.config.traefik.entrypoint_http,
        &state.config.traefik.entrypoint_https,
        state.config.traefik.dynamic_config_dir.as_deref(),
    ).await;

    // For static sites: update nginx server_name after the domain is removed.
    sync_static_nginx_conf(&state.db, service_id, &state.config.data_dir).await;

    if let Ok(Some((project_id, org_id))) = sqlx::query_as::<_, (Uuid, Uuid)>(
        "SELECT p.id, p.org_id FROM services s JOIN projects p ON p.id = s.project_id WHERE s.id = $1",
    )
    .bind(service_id)
    .fetch_optional(&state.db)
    .await
    {
        let topic = shipyard_mqtt::topics::topology(org_id, project_id);
        let payload = MqttPayload::new("topology.changed")
            .with_meta(serde_json::json!({ "resource": "domain", "domain_id": domain_id, "reason": "deleted" }));
        state.mqtt.publish_status(&topic, &payload).await.ok();
    }

    Ok(Json(ApiResponse::ok(serde_json::json!({
        "message": "Domain deleted successfully"
    }))))
}

/// GET /services/:service_id/domains/:domain_id/check-dns
async fn check_domain_dns(
    auth_user: AuthUser,
    Path((service_id, domain_id)): Path<(Uuid, Uuid)>,
    State(state): State<AppState>,
) -> Result<Json<ApiResponse<serde_json::Value>>, ApiAppError> {
    require_service_access(&state.db, auth_user.user_id, service_id).await.map_err(ApiAppError)?;
    let row = sqlx::query_as::<_, (String,)>(
        "SELECT hostname FROM domains WHERE id = $1 AND service_id = $2",
    )
    .bind(domain_id)
    .bind(service_id)
    .fetch_optional(&state.db)
    .await
    .map_err(|e| ApiAppError(AppError::Database(e.to_string())))?;

    let hostname = match row {
        Some((h,)) => h,
        None => return Err(ApiAppError(AppError::NotFound(format!("Domain '{}' not found", domain_id)))),
    };

    let lookup_target = format!("{}:80", hostname);
    let result = tokio::time::timeout(
        std::time::Duration::from_secs(5),
        tokio::net::lookup_host(&lookup_target),
    )
    .await;

    let (resolves, addresses) = match result {
        Ok(Ok(addrs)) => {
            let ips: Vec<String> = addrs.map(|a| a.ip().to_string()).collect();
            (!ips.is_empty(), ips)
        }
        _ => (false, vec![]),
    };

    Ok(Json(ApiResponse::ok(serde_json::json!({
        "hostname": hostname,
        "resolves": resolves,
        "addresses": addresses,
    }))))
}

